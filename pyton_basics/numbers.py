# Fundamental Data Types
# int and float
print(type(2 + 4))
print(type(2 - 4))
print(type(2 * 4))
print(type(2 / 4))  # 0.5

print(type(0.00001))
print(type(5.00001))
print(type(10.56))

print(type(0))

print(type(20 + 1.1))

# 2 * 2 * 2 = 8
print(2 ** 3)

# rounded down to integer
print(2 // 4)
print(5 // 4)

# we get 2. 2 is the remainder
print(6 % 4)
